/**
 * Core user table.
 * Populated/updated on first OAuth login via Auth.js Drizzle adapter.
 * Extended with Momo-specific gamification and preference fields.
 *
 * The columns `name`, `email`, `emailVerified`, and `image` are required by
 * the Auth.js Drizzle adapter. Custom Momo fields are added alongside them.
 */
export const users = pgTable("users", {
  /** Primary key — UUID generated by the DB */
  id: uuid("id").primaryKey().defaultRandom(),

  /** Display name from the OAuth provider (required by Auth.js adapter) */
  name: text("name"),

  /** Email from the OAuth provider (required by Auth.js adapter) */
  email: text("email").unique(),

  /** Timestamp of email verification (required by Auth.js adapter) */
  emailVerified: timestamp("email_verified", { mode: "date" }),

  /** Avatar URL from the OAuth provider (required by Auth.js adapter) */
  image: text("image"),

  /** Composite identifier from the OAuth provider (e.g. "github:12345678") */
  providerId: text("provider_id").unique(),

  /** Gamification coin balance */
  coins: integer("coins").notNull().default(0),

  /** Gamification level */
  level: integer("level").notNull().default(1),

  /** Current streak length in days */
  streakCurrent: integer("streak_current").notNull().default(0),

  /** All-time maximum streak length */
  streakMax: integer("streak_max").notNull().default(0),

  /** Date of the last streak-qualifying action */
  streakLastDate: date("streak_last_date"),

  /** Month (YYYY-MM) when the streak shield was last consumed. NULL = available. */
  streakShieldUsedMonth: text("streak_shield_used_month"),

  /** Monthly budget for wishlist tracking (decimal for currency precision) */
  monthlyBudget: decimal("monthly_budget", { precision: 10, scale: 2 }),

  /** Whether the user has enabled push notifications */
  notificationEnabled: boolean("notification_enabled").notNull().default(false),

  /** Time of day for the daily notification (24h format, in the user's local timezone) */
  notificationTime: time("notification_time").notNull().default("08:00"),

  /**
   * IANA timezone identifier (e.g. "Europe/Berlin", "America/New_York").
   * Used to interpret notificationTime in the user's local time rather than UTC.
   * NULL falls back to UTC.
   */
  timezone: text("timezone"),

  /**
   * Opt-in flag for the "Due today" reminder (see sendDueTodayNotifications in lib/push.ts).
   * When true, the cron dispatcher sends a notification at the user's
   * configured notificationTime *if* there is at least one non-completed,
   * non-snoozed task whose due_date (or next_due_date, for recurring tasks)
   * is today in the user's timezone. Silent on empty — no "all clear" pings.
   * Default false so existing users don't get new noise without consent.
   */
  dueTodayReminderEnabled: boolean("due_today_reminder_enabled")
    .notNull()
    .default(false),

  /**
   * Opt-in flag for per-task recurring due reminders (see sendRecurringDueNotifications in lib/push.ts).
   * When true, the cron dispatcher sends an individual notification for each
   * recurring task whose next_due_date is today in the user's timezone.
   * Suppressed when morningBriefingEnabled is true (briefing already covers due tasks).
   * Default false so existing users don't get new noise without consent.
   */
  recurringDueReminderEnabled: boolean("recurring_due_reminder_enabled")
    .notNull()
    .default(false),

  /**
   * Opt-in daily digest: consolidates quest + due tasks + streak + achievements
   * into one push at `morningBriefingTime`. Replaces individual quest and
   * due-today reminders when enabled.
   */
  morningBriefingEnabled: boolean("morning_briefing_enabled")
    .notNull()
    .default(false),

  /**
   * Time of day for the morning briefing (24h format, user's local timezone).
   * Separate from notificationTime so users can get the digest earlier/later
   * than individual reminders. Default: 08:00.
   */
  morningBriefingTime: time("morning_briefing_time").notNull().default("08:00"),

  /** User's preferred colour theme */
  theme: themeEnum("theme").notNull().default("system"),

  /** How many times the user has postponed their daily quest today */
  questPostponesToday: integer("quest_postpones_today").notNull().default(0),

  /** Date on which questPostponesToday was last incremented — used for daily reset */
  questPostponedDate: date("quest_postponed_date"),

  /** Maximum number of daily quest postponements allowed per day (1–5, default 3) */
  questPostponeLimit: integer("quest_postpone_limit").notNull().default(3),

  /**
   * Cumulative count of tasks ever created by this user.
   * Incremented on createTask() and never decremented — gives a true "tasks created" stat
   * even after tasks are deleted.
   */
  totalTasksCreated: integer("total_tasks_created").notNull().default(0),

  /** Whether to show an affirmation/quote after completing the daily quest (default: on) */
  emotionalClosureEnabled: boolean("emotional_closure_enabled").notNull().default(true),

  /** Today's self-reported energy level for daily quest matching. Null = not yet checked in today. */
  energyLevel: energyLevelEnum("energy_level"),

  /** Date on which energyLevel was set (YYYY-MM-DD in user's timezone) — used for daily reset */
  energyLevelDate: date("energy_level_date"),

  /**
   * TOTP secret for two-factor authentication, encrypted at rest with
   * AES-256-GCM (see lib/utils/crypto.ts and TOTP_ENCRYPTION_KEY env var).
   * NULL when 2FA is not configured. The plaintext secret is never stored.
   */
  totpSecret: text("totp_secret"),

  /**
   * Timestamp when the user successfully verified their TOTP setup with
   * the first valid code. While NULL, 2FA is treated as inactive even if
   * totpSecret is set (defensive — should not occur in practice because
   * setup is committed atomically). Acts as the source of truth for
   * "is 2FA enabled for this user".
   */
  totpEnabledAt: timestamp("totp_enabled_at", { withTimezone: true }),

  /**
   * SHA-256 hash of the user's personal iCal calendar feed token.
   * NULL = no feed active. When set, the plaintext token forms the URL
   * `/api/calendar/<token>.ics` that third-party calendar clients
   * (Google/Apple/Outlook) subscribe to. Exactly one active token per user;
   * regenerating replaces the hash and immediately invalidates the old URL.
   * Unique to enforce O(1) lookup during feed requests.
   */
  calendarFeedTokenHash: text("calendar_feed_token_hash").unique(),

  /** Timestamp when the current calendar feed token was generated — shown in the settings UI */
  calendarFeedTokenCreatedAt: timestamp("calendar_feed_token_created_at", {
    withTimezone: true,
  }),

  /** Whether the user has completed the onboarding wizard. False for new users. */
  onboardingCompleted: boolean("onboarding_completed").notNull().default(false),

  /**
   * End date of vacation mode (YYYY-MM-DD, inclusive). Null = no active vacation.
   * When set, all RECURRING tasks have their pausedAt/pausedUntil columns populated.
   * A daily cron job auto-ends vacation once this date has passed.
   */
  vacationEndDate: date("vacation_end_date"),

  createdAt: timestamp("created_at").notNull().defaultNow(),
});

