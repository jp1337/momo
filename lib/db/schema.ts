/**
 * Topics — user-defined project buckets to group tasks.
 * Examples: "Moving", "Tax Return", "Health", "Side Project"
 */
export const topics = pgTable("topics", {
  id: uuid("id").primaryKey().defaultRandom(),

  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  title: text("title").notNull(),

  description: text("description"),

  /** Optional hex or CSS color for UI display */
  color: text("color"),

  /** Optional emoji or icon identifier */
  icon: text("icon"),

  /** Priority affects daily quest topic selection */
  priority: priorityEnum("priority").notNull().default("NORMAL"),

  /** Archived topics are hidden from the main view but not deleted */
  archived: boolean("archived").notNull().default(false),

  /**
   * Sequential topics enforce implicit ordering: only the first still-open task
   * (lowest sort_order, not snoozed) is eligible for daily quest selection.
   * Later tasks are blocked until earlier ones are completed. Opt-in per topic.
   */
  sequential: boolean("sequential").notNull().default(false),

  /**
   * Default energy level inherited by new tasks created in this topic.
   * Useful for "Sport" (HIGH) or "E-Mails beantworten" (LOW) topics — saves
   * the user from picking the level on every task. NULL = no default.
   * Existing tasks are not retro-tagged when this changes.
   */
  defaultEnergyLevel: energyLevelEnum("default_energy_level"),

  createdAt: timestamp("created_at").notNull().defaultNow(),
});

/**
 * Tasks — the core entity of Momo.
 * Supports one-time, recurring, and daily-quest-eligible types.
 */
export const tasks = pgTable("tasks", {
  id: uuid("id").primaryKey().defaultRandom(),

  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /** Optional topic association */
  topicId: uuid("topic_id").references(() => topics.id, {
    onDelete: "set null",
  }),

  title: text("title").notNull(),

  notes: text("notes"),

  /**
   * Task type:
   *  - ONE_TIME: completed once and done
   *  - RECURRING: repeats on a schedule (recurrence_interval days)
   *  - DAILY_ELIGIBLE: can be selected as the daily quest
   */
  type: taskTypeEnum("type").notNull(),

  priority: priorityEnum("priority").notNull().default("NORMAL"),

  /** For RECURRING tasks: number of days between occurrences */
  recurrenceInterval: integer("recurrence_interval"),

  /** Original due date (user-set) */
  dueDate: date("due_date"),

  /** Computed next due date for recurring tasks */
  nextDueDate: date("next_due_date"),

  /** Set when a ONE_TIME task is completed */
  completedAt: timestamp("completed_at"),

  /** Coin reward for completing this task */
  coinValue: integer("coin_value").notNull().default(1),

  /** Whether this task is currently selected as the daily quest */
  isDailyQuest: boolean("is_daily_quest").notNull().default(false),

  /** Date on which this task was last assigned as the daily quest (YYYY-MM-DD in user's timezone) */
  dailyQuestDate: date("daily_quest_date"),

  /** How many times this task has been postponed as a daily quest (across all days) */
  postponeCount: integer("postpone_count").notNull().default(0),

  /** Estimated time to complete this task in minutes (5 / 15 / 30 / 60 / null = unknown) */
  estimatedMinutes: integer("estimated_minutes"),

  /** Date until which this task is snoozed/hidden (YYYY-MM-DD). Null = active/visible. */
  snoozedUntil: date("snoozed_until"),

  /** Energy level required to complete this task. Null = matches any energy state. */
  energyLevel: energyLevelEnum("energy_level"),

  /** Sort position within a topic. Lower values appear first. Default 0, assigned sequentially on creation. */
  sortOrder: integer("sort_order").notNull().default(0),

  /**
   * Date when this task was paused (YYYY-MM-DD). Null = not paused.
   * Set by vacation mode activation; used to calculate the actual pause
   * duration when unpausing (nextDueDate shifts by `today - pausedAt` days).
   */
  pausedAt: date("paused_at"),

  /**
   * Date until which this task is paused (YYYY-MM-DD, inclusive). Null = active.
   * Paused tasks are excluded from daily quest selection, due-today notifications,
   * and the iCal feed. Distinct from snoozedUntil: pause also shifts nextDueDate
   * on deactivation and excludes the period from habit streak calculations.
   */
  pausedUntil: date("paused_until"),

  createdAt: timestamp("created_at").notNull().defaultNow(),
});

