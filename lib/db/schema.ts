/**
 * Personal Access Tokens (API Keys) for programmatic access to the Momo API.
 *
 * Each key is stored as a SHA-256 hash of the plaintext token — the plaintext
 * is only ever shown once at creation time and never persisted.
 *
 * Key format (256-bit entropy): `momo_live_<44 chars base64url(32 random bytes)>`
 *
 * Fields:
 *  - keyHash    SHA-256(plaintext key) — used for O(1) lookup at auth time
 *  - keyPrefix  First 16 chars of the plaintext + "..." for display in the UI
 *  - readonly   If true, the key may only be used for GET requests
 *  - expiresAt  NULL = never expires; otherwise refused after this timestamp
 *  - revokedAt  NULL = active; set to revoke the key immediately
 *  - lastUsedAt Updated on each successful auth — useful for activity tracking
 */
export const apiKeys = pgTable("api_keys", {
  id: uuid("id").primaryKey().defaultRandom(),

  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /** User-defined label (e.g. "Claude MCP", "Home automation") */
  name: text("name").notNull(),

  /** SHA-256 hash of the plaintext API key — indexed, O(1) lookup */
  keyHash: text("key_hash").notNull().unique(),

  /** Display prefix: first 16 chars of plaintext + "..." (never the full key) */
  keyPrefix: text("key_prefix").notNull(),

  /** If true, key may only be used for GET requests (read-only access) */
  readonly: boolean("readonly").notNull().default(false),

  /** NULL = never expires */
  expiresAt: timestamp("expires_at"),

  /** Timestamp of last successful authentication — NULL if never used */
  lastUsedAt: timestamp("last_used_at"),

  /** Set when the key is revoked — NULL means the key is still active */
  revokedAt: timestamp("revoked_at"),

  createdAt: timestamp("created_at").notNull().defaultNow(),
});

/**
 * Short-lived tokens used during the OAuth account linking flow.
 *
 * When a logged-in user wants to connect an additional OAuth provider, Momo:
 *  1. Creates a linking_request record with the user's ID and target provider
 *  2. Redirects the user through the OAuth flow
 *  3. In the OAuth callback, reads this token to find the original user and
 *     merge the new OAuth account onto them
 *
 * Tokens expire after 5 minutes to limit the attack surface.
 */
export const linkingRequests = pgTable("linking_requests", {
  id: uuid("id").primaryKey().defaultRandom(),

  /** The user who initiated the linking request */
  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /** Target OAuth provider (e.g. "github", "discord", "google", "keycloak") */
  provider: text("provider").notNull(),

  /** Tokens expire after 5 minutes */
  expiresAt: timestamp("expires_at").notNull(),

  createdAt: timestamp("created_at").notNull().defaultNow(),
});

/**
 * Cron run log — one row per cron job execution.
 * Used to track push-notification delivery history and detect stale/missing cron jobs.
 * Visible on the admin page and via /api/health (non-blocking).
 */
export const cronRuns = pgTable("cron_runs", {
  id: uuid("id").primaryKey().defaultRandom(),

  /** Cron job name (e.g. "daily-quest") */
  name: text("name").notNull(),

  /** When the cron started */
  ranAt: timestamp("ran_at", { withTimezone: true }).notNull().defaultNow(),

  /** Number of push notifications successfully sent */
  sent: integer("sent").notNull().default(0),

  /** Number of push notifications that failed */
  failed: integer("failed").notNull().default(0),

  /** Execution time in milliseconds */
  durationMs: integer("duration_ms"),
});

/**
 * Per-device push subscriptions.
 *
 * One row per (user, browser/device) combination. The push endpoint URL is
 * globally unique — it identifies a specific browser push subscription.
 * This allows a user to receive notifications on multiple devices simultaneously.
 *
 * Lifecycle:
 *  - Created/updated when user enables notifications on a device (POST /api/push/subscribe)
 *  - Deleted when user disables notifications on that device (DELETE /api/push/subscribe)
 *  - Automatically deleted by sendPushNotification() when the push service returns 410 Gone
 */
export const pushSubscriptions = pgTable("push_subscriptions", {
  id: uuid("id").primaryKey().defaultRandom(),

  /** The user this subscription belongs to */
  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /**
   * The push endpoint URL — globally unique, identifies one browser/device.
   * Used as the upsert key: subscribing twice from the same device updates the row.
   */
  endpoint: text("endpoint").notNull().unique(),

  /** Full W3C PushSubscription JSON (endpoint + keys) */
  subscription: jsonb("subscription").notNull(),

  createdAt: timestamp("created_at", { withTimezone: true }).notNull().defaultNow(),
});

/**
 * Quest postponement log — one row per daily quest postponement.
 * Used to compute precise weekly postponement counts for the weekly review feature.
 * Historical data starts accumulating from the migration date forward.
 */
export const questPostponements = pgTable("quest_postponements", {
  id: uuid("id").primaryKey().defaultRandom(),

  /** The user who postponed the quest */
  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /** The task that was postponed */
  taskId: uuid("task_id")
    .notNull()
    .references(() => tasks.id, { onDelete: "cascade" }),

  /** When the postponement happened */
  postponedAt: timestamp("postponed_at", { withTimezone: true }).notNull().defaultNow(),
});

/**
 * User-configured notification channels (ntfy.sh, Pushover, Telegram, etc.).
 *
 * Each user may have at most one channel per type. The channel-specific
 * configuration is stored as JSONB — adding new channel types requires no
 * schema migration.
 *
 * Config shapes:
 *  - ntfy:     { topic: string, server?: string }
 *  - pushover: { userKey: string, appToken: string }
 *  - telegram: { botToken: string, chatId: string }
 *  - email:    { address: string }                     (SMTP creds via env vars)
 *  - webhook:  { url: string, secret?: string }        (future)
 */
export const notificationChannels = pgTable(
  "notification_channels",
  {
    id: uuid("id").primaryKey().defaultRandom(),

    /** The user who configured this channel */
    userId: uuid("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    /** Channel type identifier (e.g. "ntfy", "pushover", "telegram") */
    type: text("type").notNull(),

    /** Channel-specific configuration as JSONB */
    config: jsonb("config").notNull(),

    /** Whether this channel is currently active */
    enabled: boolean("enabled").notNull().default(true),

    createdAt: timestamp("created_at", { withTimezone: true }).notNull().defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true }).notNull().defaultNow(),
  },
  (table) => [
    unique("notification_channels_user_id_type_unique").on(table.userId, table.type),
  ]
);

/**
 * Per-delivery log of every notification attempt.
 *
 * Each row represents a single send attempt to one channel for one user.
 * Used by the Settings → Notification History section so users can debug
 * delivery issues ("did ntfy actually fire?"). Rows older than 30 days are
 * pruned by the `notification-log-cleanup` cron job.
 */
export const notificationLog = pgTable(
  "notification_log",
  {
    id: uuid("id").primaryKey().defaultRandom(),

    /** The user who received (or should have received) this notification */
    userId: uuid("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    /** Delivery channel: "web-push", "ntfy", "pushover", "telegram", "email" */
    channel: text("channel").notNull(),

    /** Notification title (short summary shown to the user) */
    title: text("title").notNull(),

    /** Notification body text (may be null for minimal payloads) */
    body: text("body"),

    /** Delivery outcome: "sent" or "failed" */
    status: text("status").notNull(),

    /** Error message when status is "failed"; null on success */
    error: text("error"),

    sentAt: timestamp("sent_at", { withTimezone: true }).notNull().defaultNow(),
  },
  (table) => [
    index("idx_notification_log_user_sent").on(table.userId, table.sentAt),
  ]
);

/**
 * One-time backup codes for TOTP recovery.
 * Each row stores the SHA-256 hash of a single 10-character alphanumeric
 * code (mirrors the api_keys hashing pattern). Codes are generated in
 * batches of 10 during setup or regeneration; the plaintext is shown to
 * the user exactly once. `usedAt` flips a code to consumed — codes are
 * single-use to prevent replay.
 */
export const totpBackupCodes = pgTable("totp_backup_codes", {
  id: uuid("id").primaryKey().defaultRandom(),

  userId: uuid("user_id")
    .notNull()
    .references(() => users.id, { onDelete: "cascade" }),

  /** SHA-256 hex digest of the plaintext code */
  codeHash: text("code_hash").notNull(),

  /** Set when the code is consumed; NULL means the code is still valid */
  usedAt: timestamp("used_at", { withTimezone: true }),

  createdAt: timestamp("created_at", { withTimezone: true }).notNull().defaultNow(),
});

/**
 * WebAuthn / Passkey credentials.
 *
 * Follows the Auth.js authenticators schema but is managed directly by the
 * Momo webauthn module (`lib/webauthn.ts`) — we do not use the Auth.js
 * Passkey provider because it requires `session: "jwt"` and Momo uses
 * database sessions. Instead, the custom endpoints under
 * `app/api/auth/passkey/*` drive registration, login, and second-factor
 * assertions using `@simplewebauthn/server`.
 *
 * One user can have multiple passkeys (e.g. iPhone Touch ID, Windows Hello,
 * hardware key). Deleting a user cascades to all of their passkeys. Deleting
 * a single passkey revokes it — authentication attempts with that credential
 * ID will fail afterwards.
 *
 * Presence of a row in this table counts as a registered second factor for
 * the purposes of `userHasSecondFactor()` in `lib/totp.ts`.
 */
export const authenticators = pgTable(
  "authenticators",
  {
    /** Credential ID issued by the authenticator (base64url of raw bytes). */
    credentialID: text("credential_id").notNull().unique(),

    /** Owning user — cascades on delete. */
    userId: uuid("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    /**
     * Mirrors the Auth.js adapter column. We set this to the same value as
     * credentialID because Momo does not use the Auth.js Passkey provider,
     * but the column is kept for adapter compatibility and potential future
     * migration.
     */
    providerAccountId: text("provider_account_id").notNull(),

    /** COSE public key (base64url). */
    credentialPublicKey: text("credential_public_key").notNull(),

    /** Signature counter — incremented on every successful assertion. */
    counter: integer("counter").notNull(),

    /** "singleDevice" | "multiDevice" (synced passkey). */
    credentialDeviceType: text("credential_device_type").notNull(),

    /** Whether the credential is backed up to a cloud keychain. */
    credentialBackedUp: boolean("credential_backed_up").notNull(),

    /**
     * Comma-separated list of AuthenticatorTransport values
     * (e.g. "internal,hybrid"). Used to hint the browser during assertion.
     */
    transports: text("transports"),

    /**
     * User-provided display name ("iPhone", "YubiKey 5C"). Editable from
     * the settings page. NULL when the user skipped the name prompt.
     */
    name: text("name"),

    /** When this credential was registered. */
    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),

    /** Last time this credential was used for an assertion. */
    lastUsedAt: timestamp("last_used_at", { withTimezone: true }),
  },
  (table) => ({
    compositePk: primaryKey({
      columns: [table.userId, table.credentialID],
    }),
  })
);

